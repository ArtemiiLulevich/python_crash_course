countries_list = ['Japan', 'Brazil', 'USA', 'Great Britain', 'German', 'France']

print("List: " + str(countries_list))

print("Sorted list: " + str(sorted(countries_list)))

print("Sorted and reversed list: " + str(sorted(countries_list, reverse=True)))

countries_list.reverse()

print("Reversed list: " + str(countries_list))

countries_list.reverse()

print("Reversed back list: " + str(countries_list))

countries_list.sort()

print("Sorted: " + str(countries_list))

countries_list.sort(reverse=True)

print("Reversed list: " + str(countries_list))

